l1 = ["word1", "word2", "word3"]

print("Looping over list of words")
for word in l1:
    print(word)

l2 = [1, 50, 100]

print("Looping over list of numbers")
for number in l2:
    print(number)

l3 = [True, True, False]
print("Looping over list of booleans")
for boolean in l3:
    print(boolean)

l4 = range(0,5)    
print("Looping over consecutive list of numbers")
for number in l4:
    print(number)